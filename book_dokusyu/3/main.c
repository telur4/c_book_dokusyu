#include <stdio.h>
/**
 * 単項演算子(+, -)
 * 二項演算子(+, -, *, /, %)
 * 代入演算子(=)
 * 複合代入演算子(+=, -=, *=, /=, %=)
 * 特殊な演算子(++, --)
 */
int main(void)
{
    printf("%i\n", -10);    // -10
    printf("%u\n", -10);    // 4294967286
    // 符号付き整数の-10と符号なし整数の4294967286は
    // 内部的には1111 1111 1111 1111 1111 1111 1111 0110と同じになる
    return 0;
}

